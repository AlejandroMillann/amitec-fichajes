export function detectLocale(): Locale {
  if (typeof navigator === "undefined") return "es";
  const lang = navigator.language?.slice(0, 2).toLowerCase();
  if (lang === "en") return "en";
  if (lang === "fr") return "fr";
  return "es";
}

